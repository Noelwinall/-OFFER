// 根据区名获取区域信息
export function getDistrictRegion(districtName: string): "港島" | "九龍" | "新界" {
  const feature = HK_DISTRICTS_GEOJSON.features.find(
    f => f.properties.name === districtName || f.properties.nameEn === districtName
  );
  return (feature?.properties.region as "港島" | "九龍" | "新界") || "新界";
}

// 将 GeoJSON 转换为 Leaflet 可用格式
export function toLeafletFormat(geojson: typeof HK_DISTRICTS_GEOJSON) {
  return geojson.features.map(feature => ({
    name: feature.properties.name,
    nameEn: feature.properties.nameEn,
    region: feature.properties.region,
    coordinates: feature.geometry.type === "MultiPolygon"
      ? feature.geometry.coordinates.map(poly => poly[0].map(([lng, lat]) => [lat, lng]))
      : [feature.geometry.coordinates[0].map(([lng, lat]) => [lat, lng])]
  }));
}
